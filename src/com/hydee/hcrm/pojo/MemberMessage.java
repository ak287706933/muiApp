package com.hydee.hcrm.pojo;
//任务会员信息
public class MemberMessage {
	private String cardholder;
	private String birthday;
	private String memcardno;
	private String mobile;
	private String sex;
	private String age;
	/**
	 * @return the cardholder
	 */
	public String getCardholder() {
		return cardholder;
	}
	/**
	 * @param cardholder the cardholder to set
	 */
	public void setCardholder(String cardholder) {
		this.cardholder = cardholder;
	}
	/**
	 * @return the birthday
	 */
	public String getBirthday() {
		return birthday;
	}
	/**
	 * @param birthday the birthday to set
	 */
	public void setBirthday(String birthday) {
		this.birthday = birthday;
	}
	/**
	 * @return the memcardno
	 */
	public String getMemcardno() {
		return memcardno;
	}
	/**
	 * @param memcardno the memcardno to set
	 */
	public void setMemcardno(String memcardno) {
		this.memcardno = memcardno;
	}
	/**
	 * @return the mobile
	 */
	public String getMobile() {
		return mobile;
	}
	/**
	 * @param mobile the mobile to set
	 */
	public void setMobile(String mobile) {
		this.mobile = mobile;
	}
	/**
	 * @return the sex
	 */
	public String getSex() {
		return sex;
	}
	/**
	 * @param sex the sex to set
	 */
	public void setSex(String sex) {
		this.sex = sex;
	}
	/**
	 * @return the age
	 */
	public String getAge() {
		return age;
	}
	/**
	 * @param age the age to set
	 */
	public void setAge(String age) {
		this.age = age;
	}
	

}
